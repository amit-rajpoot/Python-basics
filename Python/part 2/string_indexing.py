# INDEXING
'''Indexing in Python is a way to access individual elements of sequences like lists, strings, or tuples.
 Python uses zero-based indexing,
   meaning the first element has an index of 0,
     the second element has an index of 1, and so on. '''

list = [10, 20, 30, 40]
print(list[0])  # Output: 10
print(list[2])  # Output: 30
# NOTE --> we can manuplate or change the value of string with help of indexing